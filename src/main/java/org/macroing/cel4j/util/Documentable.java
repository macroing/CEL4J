package org.macroing.cel4j.util;

/**
 * A {@code Documentable} can be written to a {@link Document}.
 * 
 * @since 1.0.0
 * @author J&#246;rgen Lundgren
 */
public interface Documentable {
	/**
	 * Writes this {@code Documentable} to a {@link Document}.
	 * <p>
	 * Returns the {@code Document}.
	 * <p>
	 * Calling this method is equivalent to the following:
	 * <pre>
	 * {@code
	 * documentable.write(new Document());
	 * }
	 * </pre>
	 * 
	 * @return the {@code Document}
	 */
	Document write();
	
	/**
	 * Writes this {@code Documentable} to {@code document}.
	 * <p>
	 * Returns {@code document}.
	 * <p>
	 * If {@code document} is {@code null}, a {@code NullPointerException} will be thrown.
	 * 
	 * @param document the {@link Document} to write to
	 * @return {@code document}
	 * @throws NullPointerException thrown if, and only if, {@code document} is {@code null}
	 */
	Document write(final Document document);
}